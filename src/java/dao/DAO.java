    public List<Account> getList(String search) throws SQLException {
        List<Account> list = new ArrayList<>();
        Connection conn = null;
        PreparedStatement stm = null;
        ResultSet rs = null;
        try {
            conn = new DBUtils().getConnection();
            if (conn != null) {
                String sql = " Select * "
                        + " FROM account "
                        ;
                stm = conn.prepareStatement(sql);
                stm.setString(1, "%" + search + "%");
                rs = stm.executeQuery();
                while (rs.next()) {
                    int id = rs.getInt("id");
                    String user = rs.getString("user");
                    String pass = rs.getString("pass");
                    
                    
                    
                    int isSell = rs.getInt("isSell");
                    int isAdmin = rs.getInt("isAdmin");
                    

                    list.add(new Account(id, user, pass, isSell, isAdmin));
                }

            }
        } catch (Exception e) {
        } finally {
            if (rs != null) {
                rs.close();
            }
            if (stm != null) {
                stm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return list;

    }
    
    
    
